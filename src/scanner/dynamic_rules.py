class RuleStatus(Enum):
    """Lifecycle status of a dynamic rule."""
    ACTIVE = auto()
    DISABLED = auto()        # Too many false positives
    PROMOTED = auto()        # Consistently high true positive rate
    EXPERIMENTAL = auto()    # Newly created, under evaluation
    RETIRED = auto()         # Manually retired


@dataclass
class RuleEffectiveness:
    """Tracks the effectiveness of a single rule over time."""
    total_matches: int = 0
    confirmed_true_positives: int = 0
    confirmed_false_positives: int = 0
    pending_review: int = 0
    last_match_timestamp: float = 0.0
    created_timestamp: float = field(default_factory=time.time)

    @property
    def true_positive_rate(self) -> float:
        confirmed = self.confirmed_true_positives + self.confirmed_false_positives
        if confirmed == 0:
            return 0.5  # Unknown = neutral
        return self.confirmed_true_positives / confirmed

    @property
    def false_positive_rate(self) -> float:
        confirmed = self.confirmed_true_positives + self.confirmed_false_positives
        if confirmed == 0:
            return 0.0
        return self.confirmed_false_positives / confirmed

    def to_dict(self) -> dict:
        return {
            "total_matches": self.total_matches,
            "confirmed_true_positives": self.confirmed_true_positives,
            "confirmed_false_positives": self.confirmed_false_positives,
            "pending_review": self.pending_review,
            "last_match_timestamp": self.last_match_timestamp,
            "created_timestamp": self.created_timestamp,
            "true_positive_rate": round(self.true_positive_rate, 3),
            "false_positive_rate": round(self.false_positive_rate, 3),
        }

    @classmethod
    def from_dict(cls, data: dict) -> "RuleEffectiveness":
        return cls(
            total_matches=data.get("total_matches", 0),
            confirmed_true_positives=data.get("confirmed_true_positives", 0),
            confirmed_false_positives=data.get("confirmed_false_positives", 0),
            pending_review=data.get("pending_review", 0),
            last_match_timestamp=data.get("last_match_timestamp", 0.0),
            created_timestamp=data.get("created_timestamp", time.time()),
        )


@dataclass
class DynamicRule:
    """
    A single dynamic analysis rule.

    Rules can be loaded from config files or generated programmatically
    by the adaptive learning or internet discovery systems.
    """
    rule_id: str
    name: str
    description: str
    pattern: str                     # Regex pattern
    vuln_class: int                  # Maps to CWE via cwe_mapping
    severity: str = "medium"         # critical, high, medium, low
    confidence: float = 0.6          # Base confidence for matches
    languages: list = field(default_factory=list)  # Empty = all languages
    frameworks: list = field(default_factory=list)  # Empty = all frameworks
    conditions: dict = field(default_factory=dict)  # Extra match conditions
    status: RuleStatus = RuleStatus.ACTIVE
    source: str = "config"           # config, adaptive, internet, manual
    tags: list = field(default_factory=list)
    effectiveness: RuleEffectiveness = field(default_factory=RuleEffectiveness)
    priority: float = 1.0            # Adaptive priority multiplier

    @property
    def is_active(self) -> bool:
        return self.status in (RuleStatus.ACTIVE, RuleStatus.PROMOTED, RuleStatus.EXPERIMENTAL)

    def matches_language(self, language: str) -> bool:
        if not self.languages:
            return True
        return language.lower() in [l.lower() for l in self.languages]

    def matches_framework(self, framework: str) -> bool:
        if not self.frameworks:
            return True
        return framework.lower() in [f.lower() for f in self.frameworks]

    def to_dict(self) -> dict:
        return {
            "rule_id": self.rule_id,
            "name": self.name,
            "description": self.description,
            "pattern": self.pattern,
            "vuln_class": self.vuln_class,
            "severity": self.severity,
            "confidence": self.confidence,
            "languages": self.languages,
            "frameworks": self.frameworks,
            "conditions": self.conditions,
            "status": self.status.name,
            "source": self.source,
            "tags": self.tags,
            "effectiveness": self.effectiveness.to_dict(),
            "priority": self.priority,
        }

    @classmethod
    def from_dict(cls, data: dict) -> "DynamicRule":
        eff_data = data.get("effectiveness", {})
        status_name = data.get("status", "ACTIVE")
        try:
            status = RuleStatus[status_name]
        except KeyError:
            status = RuleStatus.ACTIVE

        return cls(
            rule_id=data.get("rule_id", ""),
            name=data.get("name", ""),
            description=data.get("description", ""),
            pattern=data.get("pattern", ""),
            vuln_class=data.get("vuln_class", 9),
            severity=data.get("severity", "medium"),
            confidence=data.get("confidence", 0.6),
            languages=data.get("languages", []),
            frameworks=data.get("frameworks", []),
            conditions=data.get("conditions", {}),
            status=status,
            source=data.get("source", "config"),
            tags=data.get("tags", []),
            effectiveness=RuleEffectiveness.from_dict(eff_data),
            priority=data.get("priority", 1.0),
        )


