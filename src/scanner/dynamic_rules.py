"""
BAYREUTHWING — Dynamic Analysis Rules Engine

Hot-loadable, self-tuning rule engine that evolves at runtime.
Rules can be loaded from YAML/JSON files, generated by the adaptive
learning system, or created from internet-discovered vulnerability patterns.

Key Features:
    - Hot-reload rules without restarting the scanner
    - Per-rule effectiveness tracking (TP/FP rates)
    - Auto-disable rules with high false positive rates
    - Auto-promote rules with high true positive rates
    - Conditional rules (framework-specific, language-specific)
    - Parameterized rule templates
    - Integration with adaptive learning for priority adjustment

Architecture:
    ┌──────────────────────────┐
    │  DynamicRuleEngine       │
    │  - load_rules()          │
    │  - scan(code, lang)      │
    │  - hot_reload()          │
    └──────────┬───────────────┘
               │
    ┌──────────▼───────────────┐
    │  DynamicRule             │
    │  - pattern               │
    │  - conditions            │
    │  - effectiveness tracker │
    └──────────┬───────────────┘
               │
    ┌──────────▼───────────────┐
    │  RuleEffectivenessTracker│
    │  - record_hit/miss       │
    │  - TP/FP rates           │
    │  - auto-tune decisions   │
    └──────────────────────────┘
"""

