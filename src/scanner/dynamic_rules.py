    def _compile_pattern(self, rule: DynamicRule):
        """Compile and cache a rule's regex pattern."""
        try:
            self._compiled_patterns[rule.rule_id] = re.compile(
                rule.pattern, re.IGNORECASE | re.MULTILINE
            )
        except re.error:
            rule.status = RuleStatus.DISABLED

    def _load_rules_from_dir(self):
        """Load rules from the configuration directory."""
        if not os.path.exists(self.rules_dir):
            return

        for filename in os.listdir(self.rules_dir):
            if filename.endswith((".json", ".yaml", ".yml")):
                filepath = os.path.join(self.rules_dir, filename)
                try:
                    with open(filepath, "r", encoding="utf-8") as f:
                        if filename.endswith(".json"):
                            data = json.load(f)
                        else:
                            try:
                                import yaml
                                data = yaml.safe_load(f)
                            except ImportError:
                                continue

                    rules_data = data.get("rules", [data] if "rule_id" in data else [])
                    for rule_data in rules_data:
                        rule = DynamicRule.from_dict(rule_data)
                        self.rules[rule.rule_id] = rule
                        self._compile_pattern(rule)
                except (json.JSONDecodeError, KeyError, TypeError, OSError):
                    pass

    def scan(self, code: str, language: str, framework: str = "") -> list[dict]:
        """
        Scan code using all active dynamic rules.

        Args:
            code: Source code to scan.
            language: Programming language.
            framework: Detected framework (optional).

        Returns:
            List of finding dictionaries.
        """
        findings = []

        for rule_id, rule in self.rules.items():
            if not rule.is_active:
                continue

            if not rule.matches_language(language):
                continue

            if framework and not rule.matches_framework(framework):
                continue

            compiled = self._compiled_patterns.get(rule_id)
            if compiled is None:
                continue

            for match in compiled.finditer(code):
                line = code[:match.start()].count("\n") + 1
                matched_text = match.group(0)[:100]

                findings.append({
                    "vuln_class": rule.vuln_class,
                    "severity": rule.severity,
                    "confidence": rule.confidence * rule.priority,
                    "line": line,
                    "message": f"[{rule.rule_id}] {rule.name}: {rule.description}",
                    "matched_text": matched_text,
                    "source": f"dynamic_rule:{rule.source}",
                    "rule_id": rule.rule_id,
                    "tags": rule.tags,
                })

                # Track effectiveness
                rule.effectiveness.total_matches += 1
                rule.effectiveness.last_match_timestamp = time.time()
                rule.effectiveness.pending_review += 1

        return findings

    def record_feedback(self, rule_id: str, is_true_positive: bool):
        """Record human/automated feedback on a rule's match."""
        if rule_id not in self.rules:
            return

        rule = self.rules[rule_id]
        if is_true_positive:
            rule.effectiveness.confirmed_true_positives += 1
        else:
            rule.effectiveness.confirmed_false_positives += 1

        if rule.effectiveness.pending_review > 0:
            rule.effectiveness.pending_review -= 1

        # Auto-tune
        self._auto_tune_rule(rule)

    def _auto_tune_rule(self, rule: DynamicRule):
        """Automatically adjust rule status based on effectiveness."""
        total_confirmed = (
            rule.effectiveness.confirmed_true_positives +
            rule.effectiveness.confirmed_false_positives
        )

        if total_confirmed < self.MIN_SAMPLES_FOR_AUTO_TUNE:
            return

        if rule.effectiveness.false_positive_rate > self.auto_disable_threshold:
            rule.status = RuleStatus.DISABLED

        elif rule.effectiveness.true_positive_rate > self.auto_promote_threshold:
            rule.status = RuleStatus.PROMOTED
            rule.priority = min(2.0, rule.priority * 1.2)

    def add_rule(self, rule: DynamicRule):
        """Add a new rule to the engine."""
        rule.status = RuleStatus.EXPERIMENTAL
        self.rules[rule.rule_id] = rule
        self._compile_pattern(rule)

    def hot_reload(self):
        """Reload rules from the configuration directory."""
        self._load_rules_from_dir()

    def get_stats(self) -> dict:
        """Get engine statistics."""
        active = sum(1 for r in self.rules.values() if r.is_active)
        disabled = sum(1 for r in self.rules.values() if r.status == RuleStatus.DISABLED)
        promoted = sum(1 for r in self.rules.values() if r.status == RuleStatus.PROMOTED)
        total_matches = sum(r.effectiveness.total_matches for r in self.rules.values())

        return {
            "total_rules": len(self.rules),
            "active_rules": active,
            "disabled_rules": disabled,
            "promoted_rules": promoted,
            "total_matches": total_matches,
            "rules_by_source": self._count_by_source(),
        }

    def _count_by_source(self) -> dict:
        counts = {}
        for rule in self.rules.values():
            counts[rule.source] = counts.get(rule.source, 0) + 1
        return counts

    @property
    def total_rules(self) -> int:
        return len(self.rules)
